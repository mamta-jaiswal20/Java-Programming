package Variables;

public class Variables {
    public static void main(String[] args) {

        // Declare variables of different types: int, double, boolean and String.
        int num1 = 3;
        int num2 = 5;
        System.out.println(num1 * num2);

        // String Example
        String name = new String("Mamta");
        System.out.println(name.length());

        // Assigning result to another varible and printing it
        int n1 = 4;
        int n2 = 7;
        int result = n1 + n2;
        System.out.println("Result of sum is: " + result);

    }

}
